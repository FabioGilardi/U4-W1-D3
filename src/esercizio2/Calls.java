package esercizio2;

public class Calls {

    //    ATTRIBUTES
    private int time;
    private int calledNumber;


    //    CONSTRUCTOR
    public Calls(int time, int calledNumber) {
        this.time = time;
        this.calledNumber = calledNumber;
    }


    //    METHODS
    public int getTime() {
        return time;
    }

    public int getCalledNumber() {
        return calledNumber;
    }


}
